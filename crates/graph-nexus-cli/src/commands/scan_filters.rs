    #[test]
    fn unknown_extension_filters_nothing() {
        // The filter is a no-op for unsupported extensions — the user can
        // still see all unresolved refs, just without trimming.
        let refs = vec![("foo".into(), 1usize), ("bar".into(), 2usize)];
        let (kept, dropped) = filter_refs(refs, Path::new("file.unknown"));
        assert_eq!(kept.len(), 2);
        assert_eq!(dropped, 0);
    }

    #[test]
    fn filter_refs_drops_python_noise_and_keeps_project_names() {
        let refs = vec![
            ("self".into(), 1usize),
            ("ResponseReturnValue".into(), 2usize),
            ("Any".into(), 3usize),
            ("view_class".into(), 4usize),
            ("__init__".into(), 5usize),
            ("os".into(), 6usize),
        ];
        let (kept, dropped) = filter_refs(refs, Path::new("flask/views.py"));
        let names: Vec<&str> = kept.iter().map(|(n, _)| n.as_str()).collect();
        assert_eq!(names, vec!["ResponseReturnValue", "view_class"]);
        assert_eq!(dropped, 4);
    }

    #[test]
    fn no_extension_is_safe() {
        let refs = vec![("foo".into(), 1usize)];
        let (kept, dropped) = filter_refs(refs, Path::new("Makefile"));
        assert_eq!(kept.len(), 1);
        assert_eq!(dropped, 0);
    }

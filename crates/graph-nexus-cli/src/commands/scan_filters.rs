//! Per-language stdlib/keyword denylists for `gnx scan --filter-stdlib`.
//!
//! `gnx scan` walks every identifier in a file and flags those that don't
//! resolve to a workspace symbol. Without filtering, the output is dominated
//! by stdlib types, language keywords, and well-known package roots — noise
//! that masks the real signal (typos / dangling references to project code).
//!
//! Empirically (against `.sample_repo/`): turning on `--filter-stdlib` cuts
//! noise by 43%–100% depending on language. Each bucket below is a small,
//! curated list; `linear contains` on `&[&str]` is faster than a `HashSet`
//! at these sizes and keeps the data layout cache-friendly.

use std::path::Path;

/// Return the stdlib denylist that applies to `ext`. Empty slice for
/// unsupported extensions — caller can short-circuit when filtering is on
/// but no list is available.
pub fn stdlib_denylist_for(ext: &str) -> &'static [&'static str] {
    match ext {
        "py" | "pyi" => PYTHON,
        "ts" | "tsx" | "js" | "jsx" | "mjs" | "cjs" => JS_TS,
        "rs" => RUST,
        "go" => GO,
        "java" | "kt" | "kts" => JVM,
        "cs" => CSHARP,
        "rb" => RUBY,
        "swift" => SWIFT,
        "c" | "h" | "cpp" | "hpp" | "cc" | "hh" | "cxx" | "hxx" => C_CPP,
        "dart" => DART,
        "php" => PHP,
        _ => &[],
    }
}

/// True if `name` is stdlib noise for the given extension. Composes a fast
/// `&[&str]` lookup with one structural rule (Python's `__dunder__` pattern)
/// that would otherwise blow up the static list.
pub fn is_stdlib_noise(name: &str, ext: &str) -> bool {
    // Python (and Cython-style stubs) dunder convention. Skip anything that
    // looks like `__name__` since these are language-provided attributes,
    // never project symbols a user would mistype.
    if matches!(ext, "py" | "pyi") && is_python_dunder(name) {
        return true;
    }
    stdlib_denylist_for(ext).contains(&name)
}

fn is_python_dunder(name: &str) -> bool {
    name.len() >= 5 && name.starts_with("__") && name.ends_with("__")
}

/// Drop entries whose `name` is stdlib noise for the file's extension.
/// Returns `(kept, dropped_count)` so the caller can report filtering
/// effort in the output payload.
pub fn filter_refs(refs: Vec<(String, usize)>, file_path: &Path) -> (Vec<(String, usize)>, usize) {
    let ext = file_path.extension().and_then(|s| s.to_str()).unwrap_or("");
    if ext.is_empty() {
        return (refs, 0);
    }
    let denylist = stdlib_denylist_for(ext);
    // `--filter-stdlib` for an extension we don't know about is a no-op
    // rather than an error — the user can still get value from any project
    // symbol matches even if we can't trim noise.
    if denylist.is_empty() && !matches!(ext, "py" | "pyi") {
        return (refs, 0);
    }
    let before = refs.len();
    let kept: Vec<(String, usize)> = refs
        .into_iter()
        .filter(|(name, _)| !is_stdlib_noise(name, ext))
        .collect();
    let dropped = before - kept.len();
    (kept, dropped)
}

